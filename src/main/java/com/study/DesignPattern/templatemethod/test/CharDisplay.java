package com.study.DesignPattern.templatemethod.test;

import com.study.DesignPattern.templatemethod.AbstractDisplay;

public class CharDisplay implements AbstractDisplay2 {
    private char ch;

    public CharDisplay(char ch) {
        this.ch = ch;
    }

    @Override
    public void open() {
        System.out.print("<<");
    }

    @Override
    public void print() {
        System.out.print(ch);
    }

    @Override
    public void close() {
        System.out.println(">>");
    }
}
